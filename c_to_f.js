"use strict"

// formula F=9/5C+32

// Celcius to Fahrenheit

var celsiusTemperature = 36

var startingFormula = 9/5 

var startingNumber = 32

var nextStep = startingFormula * celsiusTemperature

var fahrenheitTemperature = nextStep + startingNumber

console.log("The Celcius temperature into fahrenheit is " + fahrenheitTemperature)